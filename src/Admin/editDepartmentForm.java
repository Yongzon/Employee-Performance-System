    private void loadEmployees() {
        try {
            dbConnector db = new dbConnector();
            String query = "SELECT u_id, u_fname, u_lname FROM tbl_admin WHERE u_type = 'Employee' ORDER BY u_fname";
            ResultSet rs = db.getData(query);

            dphead.removeAllItems();
            employeeMap.clear();

            dphead.addItem("Please Select Employee");

            boolean hasEmployees = false;
            while (rs.next()) {
                hasEmployees = true;
                int userId = rs.getInt("u_id");
                String fullName = rs.getString("u_fname") + " " + rs.getString("u_lname");
                dphead.addItem(fullName);
                employeeMap.put(fullName, userId);
            }

            if (!hasEmployees) {
                dphead.addItem("N/A");
                JOptionPane.showMessageDialog(this, 
                    "No employees available. Please create an user with employee type first.", 
                    "No Employees Found", 
                    JOptionPane.WARNING_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading employees: " + e.getMessage());
        }
    }
