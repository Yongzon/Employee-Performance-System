    private void saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveMouseClicked
        String departmentname = depid.getText();
        String totalemployees = totalemp.getText();
        String selectedHead = dphead.getSelectedItem().toString();  
        
        if (departmentname.isEmpty() || totalemployees.isEmpty()){
            JOptionPane.showMessageDialog(this, "All fields are required!", "Error", JOptionPane.ERROR_MESSAGE);
        }else if ("Please Select Employee".equals(selectedHead) || "N/A".equals(selectedHead)) {
        JOptionPane.showMessageDialog(this, "Please select a valid employee", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            dbConnector db = new dbConnector(); 
            Integer depId = null;
            Integer userId = employeeMap.get(selectedHead);

            try {
                String query = "SELECT * FROM tbl_admin WHERE u_id = " + userId;
                ResultSet rs = db.getData(query);
 
                if (rs.next() && rs.getInt(1) == 0) {
                JOptionPane.showMessageDialog(this, "Selected employee does not exist!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
                }   
            } catch (SQLException ex) {
                System.out.println(""+ex);
            }
            
            db.updateData ("UPDATE tbl_department SET dep_name = '"+dpname.getText()+"', dep_totalemp = '"+totalemp.getText()+"', dep_userid = '"+userId+"' WHERE dep_id = '"+depid.getText()+"'");
                Session sess = Session.getInstance();
                db.logActivity(sess.getUid(), "Updated a Departmen: " + dpname.getText());                
                JOptionPane.showMessageDialog(this, "Department Updated successfully!");
                departmentForm df = new departmentForm();
                df.setVisible(true);
                this.dispose();
            
        }
    }//GEN-LAST:event_saveMouseClicked

