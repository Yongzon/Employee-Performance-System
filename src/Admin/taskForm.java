    public void displayTasks() {
    try {
        dbConnector db = new dbConnector();
        ResultSet rs = db.getData(
                "SELECT t.t_id AS 'Task ID', "
                        + "t.t_name AS 'Task Name', "
                        + "CONCAT(a.u_fname, ' ', a.u_lname) AS 'Assigned Employee', "
                        + "t.t_description AS 'Task Description', "
                        + "t.t_deadline AS 'Deadline' "
                        + "FROM tbl_task t JOIN tbl_employee e ON t.t_empid = e.emp_id "
                        + "JOIN tbl_admin a ON e.emp_userid = a.u_id");
        tasktbl.setModel(DbUtils.resultSetToTableModel(rs));
        rs.close();
    } catch (SQLException e) {
        System.out.println("Error: " + e.getMessage());
        JOptionPane.showMessageDialog(null, 
            "Error loading tasks: " + e.getMessage(), 
            "Database Error", 
            JOptionPane.ERROR_MESSAGE);
    }
}
    
    public static int getHeightFromWidth(String imagePath, int desiredWidth) {
    try {
        // Read the image file
        File imageFile = new File(imagePath);
        BufferedImage image = ImageIO.read(imageFile);

        // Get the original width and height of the image
        int originalWidth = image.getWidth();
        int originalHeight = image.getHeight();

        // Calculate the new height based on the desired width and the aspect ratio
        int newHeight = (int) ((double) desiredWidth / originalWidth * originalHeight);

        return newHeight;
    } catch (IOException ex) {
        System.out.println("No image found!");
    }

    return -1;
    }
    
    public  ImageIcon ResizeImage(String ImagePath, byte[] pic, JLabel label) {
    ImageIcon MyImage = null;
        if(ImagePath !=null){
            MyImage = new ImageIcon(ImagePath);
        }else{
            MyImage = new ImageIcon(pic);
        }
        
    int newHeight = getHeightFromWidth(ImagePath, label.getWidth());

    Image img = MyImage.getImage();
    Image newImg = img.getScaledInstance(label.getWidth(), newHeight, Image.SCALE_SMOOTH);
    ImageIcon image = new ImageIcon(newImg);
    return image;
}
    
