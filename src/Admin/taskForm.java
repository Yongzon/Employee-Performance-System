    private void delMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_delMouseClicked
        int selectedRow = tasktbl.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please Select an Item!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this task?", "Confirm Deletion",JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            int taskId = (int) tasktbl.getValueAt(selectedRow, 0);

            try {
                dbConnector db = new dbConnector();
                db.updateData("DELETE FROM tbl_task WHERE t_id = " + taskId);

                Session sess = Session.getInstance();
                db.logActivity(sess.getUid(), "Deleted user ID: " + taskId);

                displayTasks();
                JOptionPane.showMessageDialog(this, "Task deleted successfully");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error deleting task: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_delMouseClicked

